const months = [
    'January',
    'February',
    'March',
    'April',
    'May',
    'June',
    'July',
    'August',
    'September',
    'October',
    'November',
    'December'
]

const shortMonths = [
    'JAN',
    'FEB',
    'MAR',
    'APR',
    'MAY',
    'JUN',
    'JUL',
    'AUG',
    'SEP',
    'OCT',
    'NOV',
    'DEC'
]

export function dateFormatter(data: string | Date) {
    var date = new Date(data);
    return `${date.getDay()}/${shortMonths[date.getMonth()]}/${date.getFullYear()}`
}